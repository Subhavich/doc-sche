  static holidays = ThaiHolidayCalendar.data.VCALENDAR[0].VEVENT;

  static formatDate(date = new Date()) {
    const year = date.getFullYear();
    const offSetMonth = date.getMonth() + 1;
    const month = offSetMonth < 10 ? "0" + offSetMonth : date.getMonth() + 1;
    const day = date.getDate() < 10 ? "0" + date.getDate() : date.getDate();
    return year.toString() + month.toString() + day.toString();
  }

  static isHoliday(date = new Date()) {
    const targetDate = ThaiHolidayCalendar.formatDate(date);
    const result = ThaiHolidayCalendar.holidays.find(
      (date) => date["DTSTART;VALUE=DATE"] === targetDate
    );
    return Boolean(result);
  }
